package com.scholar.literature.pojo;

public class Pub {
   private String id;
   private int rank;

   public void setId(String id) {
      this.id = id;
   }

   public void setRank(int rank) {
      this.rank = rank;
   }

   public String getId() {
      return id;
   }

   public int getRank() {
      return rank;
   }
}
